#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - sum two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: Always 0
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - subtract two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: Always 0
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiplies two number
 * @a: The first number
 * @b: The second number
 *
 * Return: ALways 0
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - divides two numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: Always 0
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod -  reminder of division
 * @a: The first number
 * @b: The second number
 *
 * Return: Always 0
 */
int op_mod(int a, int b)
{
return (a % b);
}
